// ── Language Engine ──────────────────────────────────────────
function getLang() {
  return localStorage.getItem('sylvia-lang') || 'zh';
}

function setLang(lang) {
  localStorage.setItem('sylvia-lang', lang);
}

function applyTranslations(lang) {
  document.documentElement.lang = lang === 'zh' ? 'zh-TW' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const t = TRANSLATIONS[key];
    if (!t) return;
    const text = t[lang];
    if (text === undefined) return;
    // Use innerHTML to support <br> tags
    el.innerHTML = text;
  });

  // Update toggle button label
  const btn = document.getElementById('lang-toggle');
  if (btn) {
    const labelEl = btn.querySelector('.lang-toggle__label');
    if (labelEl) {
      const t = TRANSLATIONS['nav.lang'];
      if (t) labelEl.textContent = t[lang];
    }
  }
}

function initI18n() {
  const lang = getLang();
  applyTranslations(lang);

  const btn = document.getElementById('lang-toggle');
  if (btn) {
    btn.addEventListener('click', () => {
      const current = getLang();
      const next = current === 'zh' ? 'en' : 'zh';
      setLang(next);
      applyTranslations(next);
    });
  }
}

document.addEventListener('DOMContentLoaded', initI18n);
